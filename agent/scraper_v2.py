# ── Scraping logic — V2 selectors (post-heal DOM) ───────────────────────────
def scrape_v2(html: str) -> list[dict]:
    """
    Selectors updated after DOM change detected:
      card:       .ann-item          (was: .announcement-card)
      title:      .item-heading a    (was: .ann-title a)
      category:   .ann-category-badge
      excerpt:    .ann-excerpt
      date:       time.item-date     (was: time.ann-date)
      department: .item-dept         (was: .ann-department)
    """
    soup = BeautifulSoup(html, "html.parser")
    results = []

    for card in soup.select(".ann-item"):
        title_el    = card.select_one(".ann-heading a")
        excerpt_el  = card.select_one(".ann-excerpt")
        date_el     = card.select_one("time.ann-post-date")
        dept_el     = card.select_one(".ann-dept-label")
        badge_el    = card.select_one(".ann-category-badge")

        if not title_el:
            continue

        results.append({
            "title":      title_el.get_text(strip=True),
            "category":   badge_el.get_text(strip=True) if badge_el else "Unknown",
            "excerpt":    excerpt_el.get_text(strip=True) if excerpt_el else "",
            "date":       date_el.get("datetime", "") if date_el else "",
            "department": dept_el.get_text(strip=True) if dept_el else "",
        })

    return results


def compute_dom_fingerprint(html: str) -> str:
    soup = BeautifulSoup(html, "html.parser")
    cards = len(soup.select(".announcement-card"))
    items = len(soup.select(".ann-item"))
    return f"cards={cards}|items={items}"


def save_state(data: dict):
    STATE_FILE.write_text(json.dumps(data, indent=2))

def load_state() -> dict:
    if STATE_FILE.exists():
        return json.loads(STATE_FILE.read_text())
    return {}


