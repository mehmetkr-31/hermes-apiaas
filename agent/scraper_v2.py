@app.get("/announcements", response_model=AnnouncementsResponse)
async def get_announcements():
    async with httpx.AsyncClient(timeout=10) as client:
        try:
            resp = await client.get(TARGET_URL)
            resp.raise_for_status()
        except Exception as e:
            raise HTTPException(status_code=502, detail=f"Cannot reach target: {e}")

    html = resp.text
    announcements = scrape_v2(html)

    if not announcements:
        raise HTTPException(
            status_code=503,
            detail="Scraper returned 0 results. Another DOM change detected. Triggering heal."
        )

    state = {
        "last_successful_scrape": datetime.utcnow().isoformat(),
        "announcement_count": len(announcements),
        "dom_fingerprint": compute_dom_fingerprint(html),
        "selector_version": "v2"
    }
    save_state(state)

    return AnnouncementsResponse(
        source=TARGET_URL,
        scraped_at=datetime.utcnow().isoformat(),
        count=len(announcements),
        announcements=announcements
    )


@app.get("/health", response_model=HealthResponse)
async def health_check():
    state = load_state()

    async with httpx.AsyncClient(timeout=10) as client:
        try:
            resp = await client.get(TARGET_URL)
            html = resp.text
        except Exception as e:
            return HealthResponse(
                status="broken",
                last_successful_scrape=state.get("last_successful_scrape"),
                announcement_count=0,
                dom_fingerprint=None,
                message=f"Target unreachable: {e}"
            )

    announcements = scrape_v2(html)
    fingerprint   = compute_dom_fingerprint(html)

    if not announcements:
        return HealthResponse(
            status="broken",
            last_successful_scrape=state.get("last_successful_scrape"),
            announcement_count=0,
            dom_fingerprint=fingerprint,
            message="Scraper returned 0 results — DOM structure likely changed again."
        )

    return HealthResponse(
        status="ok",
        last_successful_scrape=datetime.utcnow().isoformat(),
        announcement_count=len(announcements),
        dom_fingerprint=fingerprint,
        message="All systems nominal. (v2 selectors active)"
    )


